package com.sine.model;

public class ProductItem {
  private Double highPrice;
  private Long iid;
  private String img;
  private String itemContent;
  private Double midPrice;
  private String miniImgs;
  private Integer pid;
  private Double price;
  private String propPathMap;
  private String sku;
  private Integer status;
  private Integer store;
  private String systemCode;

  public Double getHighPrice() {
    return highPrice;
  }

  public void setHighPrice(Double highPrice) {
    this.highPrice = highPrice;
  }

  public Long getIid() {
    return iid;
  }

  public void setIid(Long iid) {
    this.iid = iid;
  }

  public String getImg() {
    return img;
  }

  public void setImg(String img) {
    this.img = img;
  }

  public String getItemContent() {
    return itemContent;
  }

  public void setItemContent(String itemContent) {
    this.itemContent = itemContent;
  }

  public Double getMidPrice() {
    return midPrice;
  }

  public void setMidPrice(Double midPrice) {
    this.midPrice = midPrice;
  }

  public String getMiniImgs() {
    return miniImgs;
  }

  public void setMiniImgs(String miniImgs) {
    this.miniImgs = miniImgs;
  }

  public Integer getPid() {
    return pid;
  }

  public void setPid(Integer pid) {
    this.pid = pid;
  }

  public Double getPrice() {
    return price;
  }

  public void setPrice(Double price) {
    this.price = price;
  }

  public String getPropPathMap() {
    return propPathMap;
  }

  public void setPropPathMap(String propPathMap) {
    this.propPathMap = propPathMap;
  }

  public String getSku() {
    return sku;
  }

  public void setSku(String sku) {
    this.sku = sku;
  }

  public Integer getStatus() {
    return status;
  }

  public void setStatus(Integer status) {
    this.status = status;
  }

  public Integer getStore() {
    return store;
  }

  public void setStore(Integer store) {
    this.store = store;
  }

  public String getSystemCode() {
    return systemCode;
  }

  public void setSystemCode(String systemCode) {
    this.systemCode = systemCode;
  }


}
